package ch04;

public class BinaryTest {

	public static void main(String[] args) {
		
		int num=10;

		int bNum=0B1010;// int bNum=  0B 라는 식별자는 뒤에 나오는 것이 이진수라는 의미이다.
		int oNum=012; // 0은 8진수라는 의미
		int xNum=0XA; // 식별자는 소문자 대문자 무관하다.

		
	System.out.println(num);
	System.out.println(bNum);
	System.out.println(oNum);
	System.out.println(xNum);
	
	// 거의 쓰지 않지만 만약 사용할 경우 위와 같이 식별자를 사용해야 한다.
	
	}

}
